function getQuadrant(marketScore, compScore) {
  if (marketScore >= 60 && compScore >= 60) return '明星区';
  if (marketScore >= 60 && compScore < 60) return '机会区';
  if (marketScore < 60 && compScore < 60) return '陷阱区';
  return '保护区';
}

function calcMarketScore(answers) {
  if (!answers || answers.length !== 4) return 0;
  var sum = 0;
  for (var i = 0; i < answers.length; i++) { sum += answers[i]; }
  return Math.round(sum / answers.length * 20);
}

function calcCompScore(answers) {
  if (!answers || answers.length !== 4) return 0;
  var sum = 0;
  for (var i = 0; i < answers.length; i++) { sum += answers[i]; }
  return Math.round(sum / answers.length * 20);
}

